#Comparison Operators
# ==	Equal	x == y
# !=	Not equal	x != y
# >	Greater than	x > y
# <	Less than	x < y
# >=	Greater than or equal to	x >= y
# <=	Less than or equal to	x <= y


x=int(input('Enter the num1\t'))
y=int(input("Enter the num2\t"))

print(x==y)
print(x!=y)
print(x>y)
print(x<y)
print(x<=y)
print(x>=y)


